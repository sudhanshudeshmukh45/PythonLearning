#Variable = It is reusable Container for a value (String, Integer, Float, Boolean)
#We can use variable along with some text :  f-string

# String : It is series of characters
first_name = "Sudhanshu"
food = "Paneer"
email = "sudhanshu@gmail.com"


print(f"Hello {first_name}")
print(f"You like {food}")
print(f"Your email is {email}")

#Integer : It can be whole number
age = 25
quantity = 5
print(f"You are {age} years old")
print(f"You are buying {quantity} items")

#Float: It is a number but contains decimal portion
price = 100.25
print(f"Price of each item is ${price}")

#Boolean: Two options True or False
is_student = True
print(f"Are you a student: {is_student}")